#include "libft.h"

char	*ft_strfix(char *s)
{
	int i;

	i = -1;
	while (s[++i])
	{
		if (s[i - 1] == ' ' || i == 0 || s[i - 1] == '\t' || s[i - 1] == '\n')
			if (s[i + 1] == ' ')
			{
				if (s[i] >= 'A' && s[i] <= 'Z')
					s[i] += 32;
			}
			else
			{
				if (s[i] >= 'a' && s[i] <= 'z')
					s[i] -= 32;
			}
		else
		{
			if (s[i + 1])
				if (s[i] >= 'A' && s[i] <= 'Z')
					s[i] += 32;
		}
	}
	return (s);
}
